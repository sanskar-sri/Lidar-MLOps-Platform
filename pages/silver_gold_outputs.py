@callback(
    Output("silver-gold-context", "children"),
    Output("silver-gold-silver-table", "children"),
    Output("silver-gold-gold-table", "children"),
    Output("silver-gold-contracts", "children"),
    Output("silver-gold-paths", "children"),
    Input("selected-dataset-id", "data"),
    Input("url", "search"),
    Input("silver-gold-refresh", "n_intervals"),
)
def update_silver_gold_outputs(selected_dataset_id, search, _ticks):
    dataset_id = resolve_selected_dataset_id(search, selected_dataset_id)
    if not dataset_id:
        empty = _empty_alert("Please select a dataset from Data Explorer first.")
        empty_table = data_table(
            [],
            empty_title="No dataset selected",
            empty_detail="Please select a dataset from Data Explorer first.",
        )
        return empty, empty_table, empty_table, empty_table, empty_table

    summary = load_silver_gold_summary(dataset_id, include_airflow_status=True)
    silver_table = data_table(
        summary.get("silver"),
        empty_title="No Silver outputs",
        empty_detail=summary.get("message") or "No Silver files were found for this dataset.",
    )
    gold_table = data_table(
        summary.get("gold"),
        empty_title="No Gold outputs",
        empty_detail=summary.get("message") or "No Gold model-ready files were found for this dataset.",
    )
    paths_table = data_table(
        summary.get("paths"),
        empty_title="No output paths",
        empty_detail=summary.get("message") or "No output prefix could be resolved.",
    )
    return _context_panel(summary), silver_table, gold_table, _contracts_panel(summary), paths_table
