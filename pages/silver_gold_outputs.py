REFRESH_INTERVAL_MS = 60_000


def _empty_alert(message, color="info"):
    return dbc.Alert(message, color=color, className="mb-0")


def _workflow_color(workflow_state):
    if workflow_state == "completed":
        return "success"
    if workflow_state in {"running", "partial"}:
        return "warning"
    if workflow_state == "failed":
        return "danger"
    return "info"


def _context_panel(summary):
    dataset_id = summary.get("dataset_id") or "Not selected"
    prep_version = summary.get("prep_version") or "Not available"
    rows = [
        ("Dataset ID", dataset_id),
        ("Prep Version", prep_version),
        ("Preprocessing Status", summary.get("preprocessing_status") or "Pending"),
        ("Silver Output Count", summary.get("silver_files", 0)),
        ("Gold Output Count", summary.get("gold_files", 0)),
        ("Gold Blocks", summary.get("gold_blocks", 0)),
        (
            "Gold Ready for Training",
            "Yes" if summary.get("gold_ready_for_training") else "No",
        ),
    ]

    split_availability = summary.get("split_availability") or {}
    rows.extend(
        [
            ("Train Blocks", "Available" if split_availability.get("train") else "Missing"),
            ("Val Blocks", "Available" if split_availability.get("val") else "Missing"),
            ("Test Blocks", "Available" if split_availability.get("test") else "Missing"),
        ]
    )

    message = summary.get("message")
    children = []
    if message:
        children.append(_empty_alert(message, _workflow_color(summary.get("workflow_state"))))
    children.append(
        html.Div(
            [
                info_card(
                    dataset_id,
                    "Silver and Gold status is derived from real output files only.",
                    "Selected Dataset",
                ),
                kv_grid(rows),
            ],
            className="ops-two-col",
        )
    )
    return html.Div(children, className="ops-stack")


